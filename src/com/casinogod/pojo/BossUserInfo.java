package com.casinogod.pojo;

public class BossUserInfo {
	
	private int id;
	
	private int bossType;
	
	
	private String battleId;
	
	private int  userId;
	
	private int  bossInstance;
	
	private int result;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getBossType() {
		return bossType;
	}

	public void setBossType(int bossType) {
		this.bossType = bossType;
	}

	public String getBattleId() {
		return battleId;
	}

	public void setBattleId(String battleId) {
		this.battleId = battleId;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public int getBossInstance() {
		return bossInstance;
	}

	public void setBossInstance(int bossInstance) {
		this.bossInstance = bossInstance;
	}

	public int getResult() {
		return result;
	}

	public void setResult(int result) {
		this.result = result;
	}
	
	


	
	
	
	
	
	

}
