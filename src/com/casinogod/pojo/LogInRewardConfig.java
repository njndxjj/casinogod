public class LogInRewardConfig {
	
	//define constructors for the User class
	public LogInRewardConfig() {}
	
	private int id;
	
	private int day;
	
	private int reward;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public int getReward() {
		return reward;
	}

	public void setReward(int reward) {
		this.reward = reward;
	}
	
	

}
